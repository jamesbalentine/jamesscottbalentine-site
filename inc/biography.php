        <div id="bio-main-professional-inside-label">Professional Bio</div>
        <div id="bio-main-professional-inside-paragraph">
          <p> A prolific arranger as well as composer, Jim's arrangements have been performed by the San Antonio
Symphony, Phoenix Symphony, and others, and his big band charts are included in the libraries of big bands across the United States.

A professional performer since high school, Jim is a freelance musician, primarily as a woodwind performer (clarinets, saxophones and flutes) in big bands, pit orchestras, and an eclectic mix of orchestra, bands and ensembles that includes performances with the San Antonio Symphony, the Houston Jazz Orchestra, and backing bands for Ray Charles, Mel Torme, Maureen McGovern, Bob Hope, Tom Jones, John Denver, Johnny Mathis and many, many others. 

Professor of Music Theory and Composition at the University of Texas at San Antonio since 1989, Jim teaches
courses in music composition, theory, and jazz studies. He received bachelor's and master's degrees from the University of South Carolina and the Doctor of Musical Arts in Composition from the University of Texas at Austin. His composition teachers included Hunter Johnson, Gordon (Dick) Goodwin, Samuel Douglas, Eugene Kurtz, Barton McLean and Karl Korte. He has also served as Director of Jazz Studies at Sam Houston State University in Huntsville, Texas, and was founder and owner of One Music Square, a full line retail music store.</p>
